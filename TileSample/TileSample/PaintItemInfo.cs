namespace TileSample
{
    public class PaintItemInfo
    {
        public int TilePaint, WallPaint, TileCoating, WallCoating;

        public PaintItemInfo(int tilePaint, int wallPaint, int tileCoating, int wallCoating)
        {
            TilePaint = tilePaint;
            WallPaint = wallPaint;
            TileCoating = tileCoating;
            WallCoating = wallCoating;
        }
    }
}